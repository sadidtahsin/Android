package android.tamim.com.retrofittest.models;

import com.google.gson.annotations.SerializedName;


public class Posts {

    @SerializedName("userId")
    private int userId;

    @SerializedName("id")
    private int id;

    @SerializedName("title")
    private String title;

    @SerializedName("body")
    private String body;

    public void setUserId(int userId) {
        this.userId = userId;
    }



    public int getUserId() {
        return userId;
    }

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getBody() {
        return body;
    }
}
